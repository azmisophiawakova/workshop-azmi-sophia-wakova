# Program menghitung luas trapesium

# Input nilai
sisi_atas = 10
sisi_bawah = 14
tinggi = 8

# Rumus luas trapesium
luas = 1/2 * (sisi_atas + sisi_bawah) * tinggi

# Output hasil
print("Sisi atas =", sisi_atas)
print("Sisi bawah =", sisi_bawah)
print("Tinggi =", tinggi)
print("Luas trapesium =", luas)
